package cn.wildfirechat.pojos;

public class InputGetUserSK {
    private String uid;
    private String cid;

    public InputGetUserSK(String uid, String cid) {
        this.uid = uid;
        this.cid = cid;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }
}
